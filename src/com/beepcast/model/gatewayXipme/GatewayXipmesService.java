package com.beepcast.model.gatewayXipme;

import java.util.List;

import com.firsthop.common.log.DLog;
import com.firsthop.common.log.DLogContext;
import com.firsthop.common.log.SimpleContext;

public class GatewayXipmesService {

  // ////////////////////////////////////////////////////////////////////////////
  //
  // Constanta
  //
  // ////////////////////////////////////////////////////////////////////////////

  static final DLogContext lctx = new SimpleContext( "GatewayXipmesService" );

  // ////////////////////////////////////////////////////////////////////////////
  //
  // Data Member
  //
  // ////////////////////////////////////////////////////////////////////////////

  private GatewayXipmesDAO dao;

  // ////////////////////////////////////////////////////////////////////////////
  //
  // Constructor
  //
  // ////////////////////////////////////////////////////////////////////////////

  public GatewayXipmesService() {
    dao = new GatewayXipmesDAO();
  }

  // ////////////////////////////////////////////////////////////////////////////
  //
  // Support Function
  //
  // ////////////////////////////////////////////////////////////////////////////

  public int insert( List listGatewayXipmeBeans ) {
    int totalInserted = 0;
    if ( listGatewayXipmeBeans == null ) {
      DLog.warning( lctx , "Failed to insert "
          + ", found null listGatewayXipmeBeans" );
      return totalInserted;
    }
    totalInserted = dao.insert( listGatewayXipmeBeans );
    return totalInserted;
  }

}
